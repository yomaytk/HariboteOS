void putfont8(char *vram, int xsize, int x, int y, char c, char *font)
{
	int i;
	char *p, d /* data */;
	for (i = 0; i < 16; i++) {
		p = vram + (y + i) * xsize + x;
		d = font[i];
		if ((d & 0x80) != 0) { p[0] = c; }
		if ((d & 0x40) != 0) { p[1] = c; }
		if ((d & 0x20) != 0) { p[2] = c; }
		if ((d & 0x10) != 0) { p[3] = c; }
		if ((d & 0x08) != 0) { p[4] = c; }
		if ((d & 0x04) != 0) { p[5] = c; }
		if ((d & 0x02) != 0) { p[6] = c; }
		if ((d & 0x01) != 0) { p[7] = c; }
	}
	return;
}
void putfonts8_asc(char *vram, int xsize, int x, int y, char c, unsigned char *s)
{
	for (; *s != 0x00; s++) {
		putfont8(vram, xsize, x, y, c, hankaku + *s * 16);
		x += 8;
	}
	return;
}
int sum(int count, ...) {
	va_list ap;
	va_start(ap, count);
	
	int sum = 0;
	for (int i = 0; i < count; i++ ) {
		sum += va_arg(ap, int);
	}
	va_end(ap);
	return sum;
}
/*=== sprintの補助関数 ===*/
void sub_sp(char *s, int arg, int i, int dig){
	
	int arg2 = arg;	
	int d = 0;

	while(arg2 > 0){arg2 /= dig;d++;}
	for(int j = d-1;j >= 0;j--){
		char c = arg%dig;
		if(c < 10)	s[i+j] = 0x30+c;
		else 	s[i+j] = 0x60+(c-9);
		arg /= dig;
	}
}
/*=== sprintfの代替関数 ===*/
void sprint(int count, char *s, char *ss, ...){

	va_list itr;
	va_start(itr, count);
	int arg, arg2, d;

	for(int i = 0; ss[i] != 0x00;i++){
		if(ss[i] != '%' || ss[i+1] == 0x00){
			s[i] = ss[i];
		}else{
			if(ss[i+1] == 'd'){
				sub_sp(s, va_arg(itr, int), i, 10);
			}else if(ss[i+1] == 'x'){
				s[i++] = 0x30;	s[i++] = 0x78;	// 0x78 -> 'x'
				sub_sp(s, va_arg(itr, int), i, 16);
			}
			i += d;
		}
	}
	va_end(itr);
	return ;
}